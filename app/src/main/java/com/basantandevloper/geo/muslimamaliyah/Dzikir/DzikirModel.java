package com.basantandevloper.geo.muslimamaliyah.Dzikir;

public class DzikirModel {
    private int id;
    private String nama;
    private String hitungan;
    private String bacaan;
    private String arti;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getHitungan() {
        return hitungan;
    }

    public void setHitungan(String hitungan) {
        this.hitungan = hitungan;
    }

    public String getBacaan() {
        return bacaan;
    }

    public void setBacaan(String bacaan) {
        this.bacaan = bacaan;
    }

    public String getArti() {
        return arti;
    }

    public void setArti(String arti) {
        this.arti = arti;
    }
}
